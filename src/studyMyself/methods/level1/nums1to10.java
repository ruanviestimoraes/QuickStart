package studyMyself.methods.level1;
//Write a method that prints all numbers from 1 to 10.

public class nums1to10 {
    public static void main(String[] args) {
        nums();

    }

    public static void nums() {

        for (int i = 1; i < 11; i++) {
            System.out.println(i);
        }

    }

}
